#待切分文本
sentence ="经常有意见分歧"

#实现全切分函数，输出根据字典能够切分出的所有的切分方式
def all_cut(sentence, Dict):
    #TODO
    target = []

    def backtrack(text, path):
        # 如果文本为空，说明已经完成一种分词方式
        if not text:
            target.append(path.copy())
            return

        # 尝试不同长度的词
        for i in range(1, len(text) + 1):
            word = text[:i]
            # 如果当前词在词典中
            if word in Dict:
                # 加入当前词到路径
                path.append(word)
                # 递归处理剩余文本
                backtrack(text[i:], path)
                # 回溯，移除当前词
                path.pop()

    backtrack(sentence, [])

    return target

print(all_cut(sentence, Dict))

