#待切分文本
sentence = "经常有意见分歧"

#实现全切分函数，输出根据字典能够切分出的所有的切分方式

def all_cut(sentence, Dict):
    n = len(sentence)
    results = []
    def dfs(start, path):
        if start == n:
            results.append(path[:])
            return
        for end in range(start + 1, n + 1):
            word = sentence[start:end]
            if word in Dict:
                # 将当前词加入路径并继续搜索
                path.append(word)
                dfs(end, path)
                path.pop()

    dfs(0, [])
    return results


