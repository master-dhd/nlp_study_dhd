#词典；每个词后方存储的是其词频，词频仅为示例，不会用到，也可自行修改
Dict = {"经常":0.1,
        "经":0.05,
        "有":0.1,
        "常":0.001,
        "有意见":0.1,
        "歧":0.001,
        "意见":0.2,
        "分歧":0.2,
        "见":0.05,
        "意":0.05,
        "见分歧":0.05,
        "分":0.1}

#待切分文本
sentence = "经常有意见分歧"

def all_cut(sentence, Dict):
    if not sentence:
        return [[]]

    target = []
    for i in range(1, len(sentence) + 1):
        word = sentence[:i]  # 取前i个字符作为候选词
        if word in Dict:  # 如果候选词在词典中
            # 递归切分剩余部分
            remaining = sentence[i:]
            sub_results = all_cut(remaining, Dict)
            # 将当前词与剩余部分的所有切分结果组合
            for sub_result in sub_results:
                target.append([word] + sub_result)

    return target

